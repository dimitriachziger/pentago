package logic;

/**
 * Beschreibt die verschiedenen Quadranten.
 * 
 * @author Dimitri Solodownik, inf102736
 *
 */
public enum Quadrant {

    /**
     * Quadrant oben links
     */
    TOP_LEFT,

    /**
     * Quadrant oben rechts
     */
    TOP_RIGHT,

    /**
     * Quadrant unten links
     */
    BOTTOM_LEFT,

    /**
     * Quadrant und rechts
     */
    BOTTOM_RIGHT;

}
